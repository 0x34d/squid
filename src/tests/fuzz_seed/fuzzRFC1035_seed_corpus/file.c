void save_to_file(const char *filename, const char *data, size_t size) {
    FILE *fp = fopen(filename, "wb");
    if (fp == NULL) {
        perror("Error opening file");
        return;
    }
    fwrite(data, 1, size, fp);
    fclose(fp);
}

